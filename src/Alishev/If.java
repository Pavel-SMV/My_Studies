public class If {
    public static void main(String[] args) {
        int myInt = 15;
        if (myInt > 4) {
            System.out.println("Да верно");
        } else if (myInt > 15) {
            System.out.println("Не верно");
        } else {
            System.out.println("Ни то, ни то");
        }
    }
}

